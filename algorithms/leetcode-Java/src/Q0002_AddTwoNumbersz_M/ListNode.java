package Q0002_AddTwoNumbersz_M;

/**
 * @author Die antwort
 * @2020/2/6 18:53
 * Description:
 * Definition for singly-linked list.
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
